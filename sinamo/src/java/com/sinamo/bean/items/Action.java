package com.sinamo.bean.items;

/**
 *
 * @author romulogalindo
 */
public class Action {

    String id;
    String doit;
    String title;

    public Action() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDoit() {
        return doit;
    }

    public void setDoit(String doit) {
        this.doit = doit;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
